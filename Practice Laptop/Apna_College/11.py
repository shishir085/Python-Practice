# features in lists {array}

lists=[3,9,4,5,6,7]

lists.append(2)# add element at the end of lists
print(lists)

lists.sort() #sort in asscending order
print(lists)


lists.sort(reverse=True)# sort in descending order
print(lists)

lists.reverse()  # reverse the string
print(lists)

lists.insert(6,8)   #insert the element after a  certain index
print(lists)
lists.remove(6)# remove element at the first of lists  #the element has to be in the brackets
print(lists)


lists.pop(2)# delete element at the index
print(lists)
